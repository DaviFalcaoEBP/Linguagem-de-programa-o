# Tipo int e Float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negatico. int sem sinal é 
# Considerado positivo.

print (11) #int
print (- 11) #int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo

print(1.1, 10.11)
print (0.0 , -1,5)

# A função type mostra o tipo de python
# inferiu o valor
print (   type('Davi')  )
print (   type(1)       )
print (  type(1.1)      )

